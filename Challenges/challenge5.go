func Challenge5(useTestData bool) {
	var data []string
	if useTestData {
		data = challenge5TestData
	} else {
		data = challenge5InputData
	}

	println("Part 1")
	challenge5Part1(data)
	println("Part 2")
	challenge5Part2(data)
}

func challenge5Part1(data []string) {
	highest := 0
	for _, bp := range data {
		d, u := 0, 127
		for i := 0; i < 7; i++ {
			mid := d + (u-d)/2
			if bp[i] == 'F' {
				u = mid
			} else if bp[i] == 'B' {
				d = mid + 1
			}
		}

		r, l := 0, 7
		for i := 7; i < len(bp); i++ {
			mid := r + (l-r)/2
			if bp[i] == 'R' {
				r = mid + 1
			} else if bp[i] == 'L' {
				l = mid
			}
		}

		highest = max(highest, d*8+r)
	}

	println(highest)
}

func challenge5Part2(data []string) {
	seats := []int{}

	for _, bp := range data {
		d, u := 0, 127
		for i := 0; i < 7; i++ {
			mid := d + (u-d)/2
			if bp[i] == 'F' {
				u = mid
			} else if bp[i] == 'B' {
				d = mid + 1
			}
		}

		r, l := 0, 7
		for i := 7; i < len(bp); i++ {
			mid := r + (l-r)/2
			if bp[i] == 'R' {
				r = mid + 1
			} else if bp[i] == 'L' {
				l = mid
			}
		}

		seats = append(seats, d*8+r)
	}

	sort.Ints(seats)
	for i := 0; i < len(seats)-1; i++ {
		if seats[i]+2 == seats[i+1] {
			println(seats[i] + 1)
			break
		}
	}
}
